#Deve-se multiplicar o raio pelo valor de π e depois multiplicar por 2.
raio_circulo = float(input('Insira o raio do circulo: '))

diametro = raio_circulo * 2

circunferencia = 2 * 3.14148 * raio_circulo

print (f'O raio do circulo é: {raio_circulo}')
print (f'O diâmetro do círculo é: {diametro}')
print (f'A circunferência do círculo é: {circunferencia}')