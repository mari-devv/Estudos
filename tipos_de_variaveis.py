#Tipo int e float
#int > numero inteiro pode ser negativo ou positivo
print(34)
#float > numero com casa decimal também pode ser negativo ou positivo
print(1.1)
#a função type mostra o tipo que o python inferiu ao valor
#type > classe
print(type(1.0))
print(type("amo brenno"))
print(type(2))

#Tipo bool (boolean), sim (True) não (False), existem vários operadores para "questionar".
#Dentre eles o ==, que é um operador lógico que questiona se um valor é igual ao outro
print(10 == 10) # sim > True
print(10 == 11) # não > False
print(type(10 == 10))